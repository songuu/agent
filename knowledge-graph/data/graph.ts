/** 课程的一个单元（19 章 + 毕业项目）。 */
export interface Chapter {
  /** 稳定 id：lesson 用两位编号 "01".."19"，毕设用 "capstone"。 */
  id: string;
  /** 目录名（用于生成相对链接）。 */
  slug: string;
  title: string;
  /** 所属部分（用于全局图谱按部分聚类）。 */
  part: string;
  /** 仓库内目录（含 README.md 的那层）。 */
  dir: string;
}

/** 一个知识点 / 概念节点。 */
export interface Concept {
  /** 全局唯一 slug（kebab-case），如 "agent-loop"。 */
  id: string;
  /** 展示名，如 "Agent 循环"。 */
  label: string;
  /** 所属章节 id（必须存在于 CHAPTERS）。 */
  chapter: string;
  /** 一句话说明（可空）。 */
  summary?: string;
}

/** 概念之间的关系类型。 */
export type RelationType = "前置" | "深化" | "对比" | "应用" | "组成";

/** 一条有向关系边：from --(type)--> to。 */
export interface Relation {
  from: string;
  to: string;
  type: RelationType;
  note?: string;
}

/** 一篇关联 / 延伸文章（外部权威来源或内部交叉引用）。 */
export interface Article {
  title: string;
  url: string;
  kind: "paper" | "doc" | "blog" | "video" | "internal";
  /** 关联到哪些章节（章节 id 列表）。 */
  chapters: string[];
  note?: string;
}

