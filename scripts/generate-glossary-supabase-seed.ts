import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { GLOSSARY_TERMS } from "../knowledge-graph/data/glossary";

const outputPath = join(
  import.meta.dirname,
  "..",
  "supabase",
  "seed",
  "glossary_terms.sql",
);

function sqlString(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlTextArray(values: readonly string[]): string {
  if (values.length === 0) return "array[]::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlJsonb(value: unknown): string {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

const rows = GLOSSARY_TERMS.map((term) => {
  const metadata = {
    sourceFile: "knowledge-graph/data/glossary.ts",
    companionDoc: "docs/glossary.md",
    topic: term.topic,
  };
  return [
    sqlString(term.id),
    sqlString(term.slug),
    sqlString(term.topic),
    sqlString(term.topicLabel),
    sqlString(term.term),
    sqlString(term.definition),
    String(term.sortOrder),
    sqlTextArray(term.relatedChapters),
    sqlTextArray(term.aliases),
    sqlTextArray(term.tags),
    sqlJsonb(metadata),
  ].join(", ");
});

const sql = `-- Generated by scripts/generate-glossary-supabase-seed.ts
-- Source of truth: knowledge-graph/data/glossary.ts -> GLOSSARY_TERMS
-- Rows: ${GLOSSARY_TERMS.length}

insert into public.glossary_terms (
  term_id,
  slug,
  topic,
  topic_label,
  term,
  definition,
  sort_order,
  related_chapters,
  aliases,
  tags,
  metadata
)
values
${rows.map((row) => `  (${row})`).join(",\n")}
on conflict (slug) do update set
  term_id = excluded.term_id,
  topic = excluded.topic,
  topic_label = excluded.topic_label,
  term = excluded.term,
  definition = excluded.definition,
  sort_order = excluded.sort_order,
  related_chapters = excluded.related_chapters,
  aliases = excluded.aliases,
  tags = excluded.tags,
  metadata = excluded.metadata,
  updated_at = now();
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, sql, "utf8");
console.log(`Wrote ${GLOSSARY_TERMS.length} glossary terms to ${outputPath}`);
