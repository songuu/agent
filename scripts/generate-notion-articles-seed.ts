// 离线生成 notion_articles 的 demo seed：从样例文章输出可审计 SQL。
// 用途：在真实 Notion 同步首次运行前，让 /notion 页有确定性数据可渲染。
//
// 确定性由构造保证：固定样例 → 同输入恒定同 SQL（见 [[teaching-demo-deterministic-payoff]]）。
//
//   pnpm supabase:notion-seed

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { SAMPLE_NOTION_ARTICLES } from "../news-collector/src/notion/sample-data.ts";
import type { NotionArticle } from "../news-collector/src/notion/types.ts";

const outputPath = join(import.meta.dirname, "..", "supabase", "seed", "notion_articles.sql");

function sqlString(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlTextArray(values: readonly string[]): string {
  if (values.length === 0) return "array[]::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlJsonb(value: unknown): string {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function sqlNullable(value: string | null): string {
  return value === null ? "null" : sqlString(value);
}

function rowFor(article: NotionArticle): string {
  return [
    sqlString(article.notionPageId),
    sqlString(article.slug),
    sqlString(article.sourceKey),
    sqlString(article.title),
    sqlString(article.summary),
    sqlString(article.bodyMarkdown),
    sqlNullable(article.coverImageUrl),
    sqlTextArray(article.tags),
    sqlString(article.status),
    sqlNullable(article.publishedAt),
    sqlString(article.publishedDate),
    sqlString(article.notionUrl),
    sqlString(article.notionLastEditedTime),
    sqlJsonb(article.metadata),
  ].join(", ");
}

function main(): void {
  const rows = SAMPLE_NOTION_ARTICLES.map(rowFor);

  const sql = `-- Generated by scripts/generate-notion-articles-seed.ts (offline, from sample-data)
-- Demo seed for public.notion_articles so the /notion page has deterministic data before the live sync runs.
-- The live notion-sync upserts real articles on the same conflict key (notion_page_id).
-- Rows: ${rows.length}

insert into public.notion_articles (
  notion_page_id,
  slug,
  source_key,
  title,
  summary,
  body_markdown,
  cover_image_url,
  tags,
  status,
  published_at,
  published_date,
  notion_url,
  notion_last_edited_time,
  metadata
)
values
${rows.map((row) => `  (${row})`).join(",\n")}
on conflict (notion_page_id) do update set
  slug = excluded.slug,
  source_key = excluded.source_key,
  title = excluded.title,
  summary = excluded.summary,
  body_markdown = excluded.body_markdown,
  cover_image_url = excluded.cover_image_url,
  tags = excluded.tags,
  status = excluded.status,
  published_at = excluded.published_at,
  published_date = excluded.published_date,
  notion_url = excluded.notion_url,
  notion_last_edited_time = excluded.notion_last_edited_time,
  metadata = excluded.metadata,
  updated_at = now();
`;

  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, sql, "utf8");
  process.stdout.write(`Wrote ${rows.length} notion articles to ${outputPath}\n`);
}

main();
