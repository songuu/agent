import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { INTERVIEW_QUESTIONS } from "../knowledge-graph/data/interview-questions.ts";

const outputPath = join(
  import.meta.dirname,
  "..",
  "supabase",
  "seed",
  "interview_questions.sql",
);

function sqlString(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlTextArray(values: readonly string[]): string {
  if (values.length === 0) return "array[]::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlJsonb(value: unknown): string {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

const rows = INTERVIEW_QUESTIONS.map((question) => {
  const metadata = {
    sourceFile: "knowledge-graph/data/interview-questions.ts",
    companionDoc: "docs/career-guide.md#四高频面试题清单",
    answerSource: question.answerSource,
    sourceTitles: question.sourceTitles,
    sourceUrls: question.sourceUrls,
    confidence: question.confidence ?? null,
    rationale: question.rationale ?? null,
    plainTextDescription: question.summaryExcerpt ?? null,
    faqList: question.faqList ?? null,
  };
  return [
    sqlString(question.id),
    sqlString(question.slug),
    sqlString(question.category),
    sqlString(question.categoryLabel),
    sqlString(question.question),
    sqlString(question.collectedDate),
    sqlString(question.collectedAt),
    String(question.sortOrder),
    sqlTextArray(question.relatedChapters),
    sqlTextArray(question.tags),
    sqlString(question.answerSource),
    sqlJsonb(metadata),
  ].join(", ");
});

const sql = `-- Generated by scripts/generate-interview-questions-supabase-seed.ts
-- Source of truth: knowledge-graph/data/interview-questions.ts -> INTERVIEW_QUESTIONS
-- Rows: ${INTERVIEW_QUESTIONS.length}

insert into public.interview_questions (
  question_id,
  slug,
  category,
  category_label,
  question,
  collected_date,
  collected_at,
  sort_order,
  related_chapters,
  tags,
  answer_source,
  metadata
)
values
${rows.map((row) => `  (${row})`).join(",\n")}
on conflict (slug) do update set
  question_id = excluded.question_id,
  category = excluded.category,
  category_label = excluded.category_label,
  question = excluded.question,
  collected_date = excluded.collected_date,
  collected_at = excluded.collected_at,
  sort_order = excluded.sort_order,
  related_chapters = excluded.related_chapters,
  tags = excluded.tags,
  answer_source = excluded.answer_source,
  metadata = excluded.metadata,
  updated_at = now();
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, sql, "utf8");
console.log(`Wrote ${INTERVIEW_QUESTIONS.length} interview questions to ${outputPath}`);
