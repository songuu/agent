import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { FRONTIER_ARTICLES } from "../knowledge-graph/data/frontier-articles";

const outputPath = join(
  import.meta.dirname,
  "..",
  "supabase",
  "seed",
  "frontier_ecosystem_articles.sql",
);

function sqlString(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlTextArray(values: readonly string[]): string {
  if (values.length === 0) return "array[]::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlJsonb(value: unknown): string {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

const rows = FRONTIER_ARTICLES.map((article) => {
  const metadata = {
    sourceFile: "knowledge-graph/data/graph.ts",
    displayDateLabel: article.displayDateLabel,
    originalKind: article.kind,
    ecosystemLayer: article.ecosystemLayer,
  };
  return [
    article.id,
    article.slug,
    article.chapterId,
    article.chapterSlug,
    article.title,
    article.source,
    article.url,
    article.kind,
    article.ecosystemLayer,
    article.ecosystemLayerLabel,
    article.summary,
    article.collectedDate,
    article.collectedAt,
    article.readCount,
    article.sortOrder,
  ]
    .map((value, index) => {
      if (index === 13 || index === 14) return String(value);
      return sqlString(String(value));
    })
    .concat([
      sqlTextArray(article.tags),
      sqlTextArray(article.detailParagraphs),
      sqlJsonb(metadata),
    ])
    .join(", ");
});

const sql = `-- Generated by scripts/generate-frontier-ecosystem-supabase-seed.ts
-- Source of truth: knowledge-graph/data/graph.ts -> FRONTIER_ARTICLES
-- Rows: ${FRONTIER_ARTICLES.length}

insert into public.frontier_ecosystem_articles (
  article_id,
  slug,
  chapter_id,
  chapter_slug,
  title,
  source,
  source_url,
  kind,
  ecosystem_layer,
  ecosystem_layer_label,
  summary,
  collected_date,
  collected_at,
  read_count,
  sort_order,
  tags,
  detail_paragraphs,
  metadata
)
values
${rows.map((row) => `  (${row})`).join(",\n")}
on conflict (slug) do update set
  article_id = excluded.article_id,
  chapter_id = excluded.chapter_id,
  chapter_slug = excluded.chapter_slug,
  title = excluded.title,
  source = excluded.source,
  source_url = excluded.source_url,
  kind = excluded.kind,
  ecosystem_layer = excluded.ecosystem_layer,
  ecosystem_layer_label = excluded.ecosystem_layer_label,
  summary = excluded.summary,
  collected_date = excluded.collected_date,
  collected_at = excluded.collected_at,
  read_count = excluded.read_count,
  sort_order = excluded.sort_order,
  tags = excluded.tags,
  detail_paragraphs = excluded.detail_paragraphs,
  metadata = excluded.metadata,
  updated_at = now();
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, sql, "utf8");
console.log(`Wrote ${FRONTIER_ARTICLES.length} frontier articles to ${outputPath}`);
