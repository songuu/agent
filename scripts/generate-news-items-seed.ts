// 离线生成 news_items 的 demo seed：跑 fixtures 过完整管道（dryRun），输出可审计 SQL。
// 用途：在真实 collector 首次运行前，让 /news 页面有确定性数据可渲染。
//
// 确定性由构造保证：固定 now + fixtures + 纯函数分类 → 同输入恒定同 SQL（见 [[teaching-demo-deterministic-payoff]]）。
//
//   pnpm supabase:news-seed

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { collectOnce } from "../news-collector/src/collect.ts";
import { FIXTURE_SOURCES, fixtureFetchFeed } from "../news-collector/src/fixtures.ts";
import type { NewsItem } from "../news-collector/src/types.ts";

const outputPath = join(import.meta.dirname, "..", "supabase", "seed", "news_items.sql");
const FIXED_NOW = new Date("2026-06-17T00:00:00.000Z");

function sqlString(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlTextArray(values: readonly string[]): string {
  if (values.length === 0) return "array[]::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlJsonb(value: unknown): string {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function sqlNullableTimestamp(value: string | null): string {
  return value === null ? "null" : sqlString(value);
}

function rowFor(item: NewsItem): string {
  return [
    sqlString(item.externalId),
    sqlString(item.sourceKey),
    sqlString(item.sourceName),
    sqlString(item.sourceKind),
    sqlString(item.title),
    sqlString(item.url),
    sqlString(item.summary),
    sqlString(item.ecosystemLayer),
    sqlString(item.ecosystemLayerLabel),
    sqlTextArray(item.tags),
    sqlString(item.lang),
    sqlNullableTimestamp(item.publishedAt),
    sqlString(item.publishedDate),
    sqlString(item.collectedAt),
    sqlString(item.collectedDate),
    item.enriched ? "true" : "false",
    sqlJsonb(item.metadata),
  ].join(", ");
}

async function main(): Promise<void> {
  const report = await collectOnce({
    sources: FIXTURE_SOURCES,
    fetchFeedImpl: fixtureFetchFeed,
    now: FIXED_NOW,
    dryRun: true,
  });

  const rows = report.items.map(rowFor);

  const sql = `-- Generated by scripts/generate-news-items-seed.ts (offline, from fixtures)
-- Demo seed for public.news_items so the /news page has deterministic data before the live collector runs.
-- The live news-collector daemon upserts real items on the same conflict key (external_id).
-- Rows: ${rows.length}

insert into public.news_items (
  external_id,
  source_key,
  source_name,
  source_kind,
  title,
  url,
  summary,
  ecosystem_layer,
  ecosystem_layer_label,
  tags,
  lang,
  published_at,
  published_date,
  collected_at,
  collected_date,
  enriched,
  metadata
)
values
${rows.map((row) => `  (${row})`).join(",\n")}
on conflict (external_id) do update set
  source_key = excluded.source_key,
  source_name = excluded.source_name,
  source_kind = excluded.source_kind,
  title = excluded.title,
  url = excluded.url,
  summary = excluded.summary,
  ecosystem_layer = excluded.ecosystem_layer,
  ecosystem_layer_label = excluded.ecosystem_layer_label,
  tags = excluded.tags,
  lang = excluded.lang,
  published_at = excluded.published_at,
  published_date = excluded.published_date,
  collected_at = excluded.collected_at,
  collected_date = excluded.collected_date,
  enriched = excluded.enriched,
  metadata = excluded.metadata,
  updated_at = now();
`;

  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, sql, "utf8");
  process.stdout.write(`Wrote ${rows.length} news items to ${outputPath}\n`);
}

main().catch((error: unknown) => {
  process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
});
