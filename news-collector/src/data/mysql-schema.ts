// MySQL 8.0.19+ 内容库基线 schema。
//
// 仅覆盖本仓库拥有的五张内容表；Auth、对象存储、队列和公开读 API 是独立迁移边界。
// 时间统一存 UTC DATETIME(3)，由 mysql-content-repository.ts 转换，不能让数据库会话时区决定内容顺序。

export const MYSQL_CONTENT_SCHEMA_SQL = `
CREATE TABLE IF NOT EXISTS frontier_ecosystem_articles (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  article_id VARCHAR(191) NOT NULL,
  slug VARCHAR(512) NOT NULL,
  chapter_id VARCHAR(64) NOT NULL,
  chapter_slug VARCHAR(255) NOT NULL,
  title TEXT NOT NULL,
  source VARCHAR(255) NOT NULL,
  source_url VARCHAR(2048) NOT NULL,
  source_url_sha256 CHAR(64) GENERATED ALWAYS AS (SHA2(source_url, 256)) STORED,
  kind VARCHAR(32) NOT NULL,
  ecosystem_layer VARCHAR(32) NOT NULL,
  ecosystem_layer_label VARCHAR(255) NOT NULL,
  summary MEDIUMTEXT NOT NULL,
  collected_date DATE NOT NULL,
  collected_at DATETIME(3) NOT NULL,
  read_count INT UNSIGNED NOT NULL DEFAULT 0,
  sort_order INT NOT NULL,
  tags JSON NOT NULL,
  detail_paragraphs JSON NOT NULL,
  metadata JSON NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY frontier_slug_uq (slug),
  UNIQUE KEY frontier_source_url_uq (source_url_sha256),
  KEY frontier_chapter_sort_idx (chapter_id, sort_order),
  KEY frontier_collected_date_idx (collected_date, sort_order),
  KEY frontier_layer_sort_idx (ecosystem_layer, sort_order),
  FULLTEXT KEY frontier_search_idx (title, source, summary),
  CONSTRAINT frontier_kind_chk CHECK (kind IN ('paper', 'doc', 'blog', 'video', 'internal'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS interview_questions (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  question_id VARCHAR(191) NOT NULL,
  slug VARCHAR(512) NOT NULL,
  category VARCHAR(32) NOT NULL,
  category_label VARCHAR(255) NOT NULL,
  question MEDIUMTEXT NOT NULL,
  related_chapters JSON NOT NULL,
  answer_source MEDIUMTEXT NOT NULL,
  collected_date DATE NOT NULL,
  collected_at DATETIME(3) NOT NULL,
  sort_order INT NOT NULL,
  tags JSON NOT NULL,
  metadata JSON NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY interview_slug_uq (slug),
  KEY interview_category_sort_idx (category, sort_order),
  FULLTEXT KEY interview_search_idx (question, category_label),
  CONSTRAINT interview_category_chk CHECK (category IN ('principle', 'engineering', 'project'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS glossary_terms (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  term_id VARCHAR(191) NOT NULL,
  slug VARCHAR(512) NOT NULL,
  topic VARCHAR(64) NOT NULL,
  topic_label VARCHAR(255) NOT NULL,
  term VARCHAR(512) NOT NULL,
  definition MEDIUMTEXT NOT NULL,
  related_chapters JSON NOT NULL,
  aliases JSON NOT NULL,
  sort_order INT NOT NULL,
  tags JSON NOT NULL,
  metadata JSON NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY glossary_slug_uq (slug),
  KEY glossary_topic_sort_idx (topic, sort_order),
  FULLTEXT KEY glossary_search_idx (term, definition)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS news_items (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  external_id VARCHAR(191) NOT NULL,
  source_key VARCHAR(191) NOT NULL,
  source_name VARCHAR(255) NOT NULL,
  source_kind VARCHAR(32) NOT NULL,
  title TEXT NOT NULL,
  url VARCHAR(2048) NOT NULL,
  summary MEDIUMTEXT NOT NULL,
  content_text MEDIUMTEXT NOT NULL,
  content_excerpt TEXT NOT NULL,
  content_status VARCHAR(32) NOT NULL,
  content_fetched_at DATETIME(3) NULL,
  ecosystem_layer VARCHAR(32) NOT NULL,
  ecosystem_layer_label VARCHAR(255) NOT NULL,
  tags JSON NOT NULL,
  lang CHAR(2) NOT NULL,
  published_at DATETIME(3) NULL,
  published_date DATE NOT NULL,
  collected_at DATETIME(3) NOT NULL,
  collected_date DATE NOT NULL,
  enriched TINYINT(1) NOT NULL DEFAULT 0,
  read_count INT UNSIGNED NOT NULL DEFAULT 0,
  metadata JSON NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY news_external_id_uq (external_id),
  KEY news_published_date_idx (published_date, published_at),
  KEY news_collected_date_idx (collected_date, collected_at),
  KEY news_layer_idx (ecosystem_layer, published_date, published_at),
  KEY news_source_idx (source_key, published_date, published_at),
  KEY news_url_idx (url(191)),
  FULLTEXT KEY news_search_idx (title, source_name, summary, content_excerpt, content_text),
  CONSTRAINT news_source_kind_chk CHECK (source_kind IN ('cn-media', 'en-media', 'paper', 'community', 'vendor-blog', 'release')),
  CONSTRAINT news_content_status_chk CHECK (content_status IN ('not_fetched', 'fetched', 'empty', 'failed')),
  CONSTRAINT news_lang_chk CHECK (lang IN ('zh', 'en'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS notion_articles (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  notion_page_id CHAR(36) NOT NULL,
  slug VARCHAR(512) NOT NULL,
  source_key VARCHAR(191) NOT NULL,
  title TEXT NOT NULL,
  summary MEDIUMTEXT NOT NULL,
  body_markdown LONGTEXT NOT NULL,
  cover_image_url VARCHAR(2048) NULL,
  tags JSON NOT NULL,
  status VARCHAR(16) NOT NULL,
  published_at DATETIME(3) NULL,
  published_date DATE NOT NULL,
  notion_url VARCHAR(2048) NOT NULL,
  notion_last_edited_time DATETIME(3) NOT NULL,
  read_count INT UNSIGNED NOT NULL DEFAULT 0,
  metadata JSON NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY notion_page_id_uq (notion_page_id),
  UNIQUE KEY notion_slug_uq (slug),
  KEY notion_published_date_idx (published_date, published_at),
  KEY notion_last_edited_idx (source_key, notion_last_edited_time),
  KEY notion_status_idx (status, published_date),
  FULLTEXT KEY notion_search_idx (title, summary, body_markdown),
  CONSTRAINT notion_status_chk CHECK (status IN ('draft', 'published', 'archived'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
`.trim();
